package sub;

public class Var03 {

    public static void main(String[] args) {

        /*
         * ===============================
         * 변수 명명 규칙
         * ===============================
         *
         * 반드시 지켜야 하는 규칙 (어기면 컴파일 에러)
         *  1. 같은 블록 안에서 동일한 변수명 중복 불가
         *  2. 예약어(키워드)는 변수명으로 사용 불가
         *  3. 대소문자 구분
         *  4. 숫자로 시작 불가
         *  5. 특수기호는 _ 와 $ 만 사용 가능
         *
         * 관례적으로 지켜야 하는 규칙 (어겨도 에러는 없지만 비권장)
         *  1. 첫 글자는 소문자로 시작
         *  2. 합성어는 camelCase 사용
         *  3. 의미가 명확한 이름 사용
         *  4. 변수명은 명사형으로 작성
         *  5. boolean 변수는 is, has 등으로 시작
         *  6. 한글 변수명 사용 비권장
         *  7. 언더스코어(_) 사용 비권장 (상수 제외)
         */


        // ===============================
        // 1. 반드시 지켜야 하는 규칙
        // ===============================

        // 1-1. 같은 블록 안에서 동일한 변수명 중복 불가
        int score = 80;
        // int score = 90; // 컴파일 에러

        // 1-2. 예약어는 변수명으로 사용 불가
        // int class = 1;  // 컴파일 에러 (class는 예약어)
        // int while = 10; // 컴파일 에러 (while은 예약어)

        // 1-3. 대소문자 구분 (score 와 Score 는 다른 변수)
        int Score = 75;

        // 1-4. 숫자로 시작 불가
        // int 2level = 5; // 컴파일 에러
        int level2 = 5;    // 숫자가 뒤에 오는 것은 허용

        // 1-5. 특수기호는 _ 와 $ 만 사용 가능
        // int user@id = 1; // 컴파일 에러
        int _count = 10;    // 가능 (하지만 관례상 비권장)
        int $price = 5000;  // 가능 (하지만 관례상 비권장)


        // ===============================
        // 2. 관례적으로 지켜야 하는 규칙
        // ===============================

        // 2-1. 첫 글자 소문자 + 합성어는 camelCase
        int maxScore = 100;   // 권장
        int minscore = 0;     // 비권장 (대문자 없음)
        int MaxScore = 100;   // 비권장 (첫 글자 대문자)

        // 2-2. 의미가 명확한 이름
        String temp;          // 비권장 (의미 모호)
        String emailAddress;  // 권장 (의미 명확)

        // 2-3. 변수명은 명사형
        String running;  // 비권장 (동사형)
        String speed;    // 권장 (명사형)

        // 2-4. boolean 변수는 긍정형으로 작성
        boolean isLogin   = true;   // 권장
        boolean hasCoupon = false;  // 권장
        boolean login     = true;   // 비권장

        // 2-5. 한글 변수명 비권장
        int 점수 = 100;       // 가능하지만 비권장
        int userScore = 100;  // 권장

        // 2-6. 상수는 SNAKE_CASE (대문자 + 언더스코어)
        final int MAX_SIZE  = 100;   // 권장
        final int maxSize   = 100;   // 비권장 (상수인지 구분이 안 됨)
    }
}