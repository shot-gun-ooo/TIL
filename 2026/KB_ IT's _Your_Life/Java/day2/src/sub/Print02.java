package sub;

public class Print02 {

    public static void main(String[] args) {

        /*
         * ===============================
         * System.out.printf("포맷", 값, 값, ...)
         * ===============================
         *
         * 형식을 지정해서 출력하는 메소드
         * println과 달리 자동으로 줄바꿈을 하지 않는다
         * 줄바꿈이 필요하면 \n 또는 %n을 사용한다
         *
         * IDE 단축키
         * souf + Tab → System.out.printf()
         *
         * ===============================
         * 포맷 지정자 (형식 기호)
         * ===============================
         * %d  : 정수
         * %f  : 실수
         * %c  : 문자(char)
         * %s  : 문자열(String)
         *
         * ===============================
         * 정렬 및 출력 형식
         * ===============================
         * %5d   : 전체 5칸, 오른쪽 정렬
         * %-5d  : 전체 5칸, 왼쪽 정렬
         * %05d  : 전체 5칸, 빈 공간을 0으로 채움
         * %.2f  : 소수점 아래 2자리까지 출력
         * %10.2f: 전체 10칸, 소수점 아래 2자리
         */


        // ===============================
        // 1. 기본 포맷 지정자
        // ===============================
        int iNum1 = 10;
        int iNum2 = 20;

        System.out.printf("%d\n", iNum1);               // 10
        System.out.printf("%d %d\n", iNum1, iNum2);     // 10 20
        System.out.printf("%d + %d = %d\n", iNum1, iNum2, iNum1 + iNum2); // 10 + 20 = 30

        // 포맷보다 인자가 많으면 초과 인자는 무시
        System.out.printf("%d\n", iNum1, iNum2);        // 10

        // 포맷보다 인자가 부족하면 런타임 에러 발생
        // System.out.printf("%d %d\n", iNum1);         // 에러


        // ===============================
        // 2. 실수 출력
        // ===============================
        float fNum = 1.2345458f;
        double dNum = 4.53;

        // %f : 기본적으로 소수점 아래 6자리 출력
        System.out.printf("%f\n", fNum);         // 1.234546
        System.out.printf("%f\n", dNum);         // 4.530000

        // 소수점 자리수 지정
        System.out.printf("%.2f\n", fNum);       // 1.23
        System.out.printf("%.3f\n", dNum);       // 4.530


        // ===============================
        // 3. 문자, 문자열 출력
        // ===============================
        char ch = 'a';
        String str = "Hello";

        System.out.printf("%c %s\n", ch, str);   // a Hello
        System.out.printf("%c %s\n", ch, ch);    // a a  (%s도 char 출력 가능)

        // 대문자로 변환 출력
        System.out.printf("%C %S\n", ch, str);   // A HELLO


        // ===============================
        // 4. 정렬 및 채우기
        // ===============================
        int value = 123;

        System.out.printf("[%d]\n", value);      // [123]     기본
        System.out.printf("[%6d]\n", value);     // [   123]  오른쪽 정렬 (6칸)
        System.out.printf("[%-6d]\n", value);    // [123   ]  왼쪽 정렬 (6칸)
        System.out.printf("[%06d]\n", value);    // [000123]  0으로 채움 (6칸)



        // 원의 넓이 출력
        // %d
        //   - 정수 출력 (칸 지정 없음, 값 그대로 출력)
        //   - 출력 예: "10"
        //
        //  %10.2f
        //   - %f  : 실수 출력
        //   - 10  : 전체 10칸 확보, 오른쪽 정렬
        //   - .2  : 소수점 아래 2자리까지 출력 (3자리부터 반올림)
        //   - 출력 예: "    314.16"  (앞에 공백 4칸)
        double area = 3.14159 * 10 * 10;
        System.out.printf("반지름이 %d인 원의 넓이: %10.2f\n", 10, area);


        System.out.printf("%6d | %-10s | %10s\n", 1, "홍길동", "개발자");
        System.out.printf("%6d | %-10s | %10s\n", 2, "김철수", "디자이너");
        System.out.printf("%6d | %-10s | %10s\n", 3, "이영희", "기획자");
        // 테이블 형태 출력
        //
        // 포맷 분석: "%6d | %-10s | %10s\n"
        //
        //  %6d
        //   - %d  : 정수 출력
        //   - 6   : 전체 6칸 확보, 오른쪽 정렬 (숫자가 짧으면 왼쪽에 공백 채움)
        //   - 출력 예: "     1"  (1 앞에 공백 5칸)
        //
        //  %-10s
        //   - %s  : 문자열 출력
        //   - 10  : 전체 10칸 확보
        //   - -   : 왼쪽 정렬 (이름이 짧으면 오른쪽에 공백 채움)
        //   - 출력 예: "홍길동       "  (홍길동 뒤에 공백 7칸)
        //
        //  %10s
        //   - %s  : 문자열 출력
        //   - 10  : 전체 10칸 확보, 오른쪽 정렬 (직업이 짧으면 왼쪽에 공백 채움)
        //   - 출력 예: "       개발자"  (개발자 앞에 공백 7칸)
        //
        // 실제 출력 결과 (| 로 칸이 맞춰짐):
        //      1 | 홍길동        |        개발자
        //      2 | 김철수        |       디자이너
        //      3 | 이영희        |        기획자

    }
}