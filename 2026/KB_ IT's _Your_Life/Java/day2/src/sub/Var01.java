package sub;

public class Var01 {

    public static void main(String[] args) {

        /*
         * ===============================
         * 변수(Variable)란?
         * ===============================
         *
         * 값을 저장하기 위해 메모리 공간을 확보하고
         * 그 공간에 이름을 붙인 것
         *
         * [선언 형식]
         *   자료형 변수명;
         *
         * 변수를 사용하는 이유
         *  1. 값에 의미를 부여할 수 있다
         *  2. 같은 값을 여러 곳에서 재사용할 수 있다
         *  3. 코드 흐름에 따라 변하는 값을 저장할 수 있다
         */


        // ===============================
        // 1. 값에 의미 부여하기
        // ===============================
        // 숫자만 있으면 무슨 값인지 알기 어렵다
        // 변수에 담으면 의미가 명확해진다
        int salary = 1_000_000;  // 급여
        int bonus  = 2_000;      // 보너스

        System.out.println("급여와 보너스의 합: " + (salary + bonus));


        // ===============================
        // 2. 값 재사용
        // ===============================
        // 변수 하나만 바꾸면 출력 결과가 모두 바뀐다
        int point = 20_000;

        for (int i = 1; i <= 5; i++) {
            System.out.println(i + "번 포인트: " + point);
        }


        // ===============================
        // 3. 코드 흐름에 따라 변하는 값 저장
        // ===============================
        int sum = 0;

        sum += 10;
        System.out.println(sum);  // 10

        sum += 10;
        System.out.println(sum);  // 20

        sum += 10;
        System.out.println(sum);  // 30


        // ===============================
        // 4. 변수 선언과 초기화
        // ===============================
        int a;       // 선언만 한 상태 (초기화 전에는 사용 불가)
        a = 1000;    // 초기화
        a = 2000;    // 값 변경 (재할당)

        int b = 3000; // 선언과 동시에 초기화 (권장)

        System.out.println(a);
        System.out.println(b);

        // 지역 변수는 반드시 초기화 후 사용해야 한다
        // int c;
        // System.out.println(c); // 컴파일 에러

        // 참조 자료형은 null로 초기화 가능  // null 객체가 없음
        String day = null;
        System.out.println(day);  // null


        // ===============================
        // 5. 상수 (final)
        // ===============================
        // final 키워드를 붙이면 값 변경 불가
        // 관례: 대문자 + 언더스코어(SNAKE_CASE)
        final String COMPANY = "멀티캠퍼스";
        // COMPANY = "변경시도"; // 컴파일 에러

        System.out.println(COMPANY);


        // ===============================
        // 6. 변수 값 교환 (temp 패턴)
        // ===============================
        // 두 변수의 값을 맞바꾸려면 임시 변수가 필요하다
        // 직접 x = y 로 대입하면 원래 x 값이 사라진다
        int x = 3;
        int y = 5;
        System.out.println("교환 전 x: " + x + ", y: " + y);

        int temp = x;  // x 값을 임시 보관
        x = y;         // x 에 y 값 대입
        y = temp;      // y 에 임시 보관한 x 값 대입

        System.out.println("교환 후 x: " + x + ", y: " + y);


        // ===============================
        // 7. 변수 스코프 (scope)
        // ===============================
        // 변수는 선언된 블록 { } 안에서만 사용 가능하다
        // 블록을 벗어나면 변수가 사라진다
        int v1 = 15;

        if (v1 > 10) {
            int v2 = v1 - 10;  // v2는 if 블록 안에서만 사용 가능
            System.out.println("v2: " + v2);
        }

        // System.out.println(v2); // 컴파일 에러 (블록 밖에서 접근 불가)
    }
}